export function colorSelector(type : string) : { background: string; text: string;} {
    if (type === "bug") {
        return { background: "bg-bug", text: "text-bug" }
    }

    if (type === "dark") {
        return { background: "bg-dark", text: "text-dark" }
    }

    if (type === "dragon") {
        return { background: "bg-dragon", text: "text-dragon" }
    }

    if (type === "electric") {
        return { background: "bg-electric", text: "text-electric" }
    }

    if (type === "fairy") {
        return { background: "bg-fairy", text: "text-fairy" }
    }

    if (type === "fighting") {
        return { background: "bg-fighting", text: "text-fighting" }
    }

    if (type === "fire") {
        return { background: "bg-fire", text: "text-fire" }
    }

    if (type === "flying") {
        return { background: "bg-flying", text: "text-flying" }
    }

    if (type === "ghost") {
        return { background: "bg-ghost", text: "text-ghost" }
    }

    if (type === "normal") {
        return { background: "bg-normal", text: "text-normal" }
    }

    if (type === "grass") {
        return { background: "bg-grass", text: "text-grass" }
    }

    if (type === "ground") {
        return { background: "bg-ground", text: "text-ground" }
    }

    if (type === "ice") {
        return { background: "bg-ice", text: "text-ice" }
    }

    if (type === "poison") {
        return { background: "bg-poison", text: "text-poison" }
    }

    if (type === "psychic") {
        return { background: "bg-psychic", text: "text-psychic" }
    }

    if (type === "rock") {
        return { background: "bg-rock", text: "text-rock" }
    }

    if (type === "steel") {
        return { background: "bg-steel", text: "text-steel" }
    }

    if (type === "water") {
        return { background: "bg-water", text: "text-water" }
    }
}